package com.apps.drpersonalmanager.model;

public class Exercise {

    private String nomeExerc, catExerc, descExerc;

    public Exercise() {
    }

    public String getNomeExerc() {
        return nomeExerc;
    }

    public void setNomeExerc(String nomeExerc) {
        this.nomeExerc = nomeExerc;
    }

    public String getCatExerc() {
        return catExerc;
    }

    public void setCatExerc(String catExerc) {
        this.catExerc = catExerc;
    }

    public String getDescExerc() {
        return descExerc;
    }

    public void setDescExerc(String descExerc) {
        this.descExerc = descExerc;
    }
}
